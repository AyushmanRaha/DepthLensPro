const assert = require('assert');
const fs = require('fs');
const path = require('path');
const source = fs.readFileSync(path.join(__dirname, 'main.js'), 'utf8');
const dotVenv = source.indexOf('path.join(root, ".venv", "bin", "python3")');
const venv = source.indexOf('path.join(root, "venv", "bin", "python3")');
const system = source.indexOf('candidates.push("python3")');
assert(dotVenv >= 0, '.venv/bin/python3 candidate is missing');
assert(venv > dotVenv, 'venv/bin/python3 must come after .venv/bin/python3');
assert(system > venv, 'PATH python3 must come after repo virtualenv candidates');
const winDot = source.indexOf('path.join(root, ".venv", "Scripts", "python.exe")');
const winVenv = source.indexOf('path.join(root, "venv", "Scripts", "python.exe")');
assert(winDot >= 0 && winVenv > winDot, 'Windows .venv python.exe must precede venv python.exe in dev mode');
assert(source.includes('if (isDev)') && source.includes('} else {\n      candidates.push(path.join(root, "venv", "bin", "python3"));'), 'packaged POSIX venv behavior should remain present');
console.log('Python candidate ordering tests passed.');
